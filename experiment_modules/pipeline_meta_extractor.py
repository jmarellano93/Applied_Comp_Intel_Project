# --- MATHEMATICAL UTILITY: HOPKINS STATISTIC ---
# Defends Rank 6: Evaluates the spatial clustering tendency of the data.
def calculate_hopkins(X):
    d = X.shape[1]
    n = X.shape[0]
    m = min(int(0.1 * n), 100)  # Sample size

    if m == 0:
        return 0.5

    nbrs = NearestNeighbors(n_neighbors=2).fit(X)

    # Distance to nearest neighbor for random real points (u)
    sample_X = X[np.random.choice(n, m, replace=False)]
    u_distances, _ = nbrs.kneighbors(sample_X, n_neighbors=2)
    u_distances = u_distances[:, 1]

    # Distance to nearest neighbor for random artificial points (w)
    min_vals, max_vals = X.min(axis=0), X.max(axis=0)
    random_points = np.random.uniform(min_vals, max_vals, (m, d))
    w_distances, _ = nbrs.kneighbors(random_points, n_neighbors=1)

    u_sum = np.sum(u_distances)
    w_sum = np.sum(w_distances)

    if (u_sum + w_sum) == 0:
        return 0.5

    return w_sum / (u_sum + w_sum)


def find_local_dataset_file(did, dataset_name, save_dir):
    """
    Finds a previously downloaded dataset CSV.

    Supports filenames produced by the previous downloader, e.g.:
        3_kr-vs-kp.csv
        31_credit-g.csv

    Falls back to any CSV beginning with the dataset ID.
    """
    did = int(did)

    # Primary pattern: any file beginning with "<did>_"
    pattern = os.path.join(save_dir, f"{did}_*.csv")
    matches = glob.glob(pattern)

    if matches:
        return matches[0]

    # Fallback pattern: any CSV beginning with the dataset ID
    pattern = os.path.join(save_dir, f"{did}*.csv")
    matches = glob.glob(pattern)

    if matches:
        return matches[0]

    raise FileNotFoundError(
        f"No local CSV file found for dataset ID {did} ({dataset_name}) in {save_dir}"
    )


def read_local_dataset_csv(file_path):
    """
    Reads a locally saved OpenML dataset CSV.

    The previously generated downloader saved files with comma delimiters.
    A semicolon fallback is included for compatibility with older local exports.
    """
    try:
        df = pd.read_csv(file_path)
    except UnicodeDecodeError:
        df = pd.read_csv(file_path, encoding="latin1")

    # If the file was semicolon-delimited, pandas may read it as one wide string column.
    if df.shape[1] == 1:
        try:
            df_semicolon = pd.read_csv(file_path, sep=";")
        except UnicodeDecodeError:
            df_semicolon = pd.read_csv(file_path, sep=";", encoding="latin1")

        if df_semicolon.shape[1] > 1:
            df = df_semicolon

    return df


def split_features_and_target(combined_df, did):
    """
    Splits a locally saved CSV into X and y.

    Uses OpenML metadata only, with download_data=False, to recover the target column name.
    This does not re-download the dataset contents.
    """
    dataset = openml.datasets.get_dataset(did, download_data=False)
    target_col = dataset.default_target_attribute

    if target_col is not None and target_col in combined_df.columns:
        y = combined_df[target_col]
        X = combined_df.drop(columns=[target_col])
    elif "TARGET" in combined_df.columns:
        y = combined_df["TARGET"]
        X = combined_df.drop(columns=["TARGET"])
    elif "target" in combined_df.columns:
        y = combined_df["target"]
        X = combined_df.drop(columns=["target"])
    else:
        # Fallback for manually saved files where the target is the last column
        target_col = combined_df.columns[-1]
        y = combined_df[target_col]
        X = combined_df.drop(columns=[target_col])

    return X, y


def clean_features_for_math(X):
    """
    Converts feature data into a fully numeric, imputed matrix suitable for PCA,
    Hopkins statistic, silhouette score, and Davies-Bouldin score.
    """
    X_clean = X.copy()

    # Normalize placeholder missing values that may appear as strings
    X_clean = X_clean.replace(
        ["?", "NA", "N/A", "nan", "NaN", "None", ""],
        np.nan
    )

    # Identify categorical columns
    cat_cols = X_clean.select_dtypes(include=["object", "category"]).columns.tolist()
    num_cols = [col for col in X_clean.columns if col not in cat_cols]

    # Coerce numeric columns safely
    for col in num_cols:
        X_clean[col] = pd.to_numeric(X_clean[col], errors="coerce")

    # Impute numeric columns with median
    if len(num_cols) > 0:
        X_clean[num_cols] = SimpleImputer(strategy="median").fit_transform(X_clean[num_cols])

    # Impute categorical columns with most frequent value, then ordinal encode
    if len(cat_cols) > 0:
        X_clean[cat_cols] = SimpleImputer(strategy="most_frequent").fit_transform(X_clean[cat_cols])
        X_clean[cat_cols] = OrdinalEncoder(
            handle_unknown="use_encoded_value",
            unknown_value=-1
        ).fit_transform(X_clean[cat_cols])

    # Final fallback in case any edge-case NaNs remain
    X_clean = pd.DataFrame(X_clean, columns=X.columns)
    X_clean = X_clean.apply(pd.to_numeric, errors="coerce")
    X_clean = X_clean.fillna(0)

    X_scaled = StandardScaler().fit_transform(X_clean)

    # Final numeric safety guard
    X_scaled = np.nan_to_num(
        X_scaled,
        nan=0.0,
        posinf=0.0,
        neginf=0.0
    )

    return X_clean, X_scaled


def clean_target_labels(y):
    """
    Converts target labels into clean string labels to avoid mixed-type errors
    in entropy, np.unique, silhouette score, and Davies-Bouldin score.
    """
    y_clean = pd.Series(y).replace(
        ["?", "NA", "N/A", "nan", "NaN", "None", ""],
        np.nan
    )

    y_clean = y_clean.fillna("MISSING_TARGET").astype(str)

    return y_clean


# 2. Main Execution Block
def main():
    print(f"Loading candidate list from {CSV_PATH}...")
    candidates = pd.read_csv(CSV_PATH)

    if not os.path.isdir(SAVE_DIR):
        raise FileNotFoundError(
            f"Dataset directory does not exist: {SAVE_DIR}"
        )

    meta_features_list = []

    print(
        f"Loading {len(candidates)} previously downloaded datasets from {SAVE_DIR} "
        "and extracting Elite 8 Meta-Features..."
    )

    for index, row in tqdm(candidates.iterrows(), total=len(candidates)):
        did = int(row["did"])
        name = row["name"]

        try:
            # Task 1: Load previously downloaded dataset from local CSV
            file_path = find_local_dataset_file(did, name, SAVE_DIR)
            combined_df = read_local_dataset_csv(file_path)

            # Recover target column from local CSV
            X, y = split_features_and_target(combined_df, did)

            # --- DATA CLEANING FOR MATH OPERATIONS ---
            # Math fails on strings. We must Ordinal Encode categorical columns.
            # Some CC18 datasets contain missing values or mixed-type columns, so we impute robustly.
            X_clean, X_scaled = clean_features_for_math(X)
            y_clean = clean_target_labels(y)

            # Task 2: Calculate the Elite 8 Meta-Features

            # 1. Dimensionality Ratio (N/M)
            n_d_ratio = X.shape[0] / max(X.shape[1], 1)

            # 2. Feature Kurtosis (Mean across all features, ignoring zero-variance NaNs)
            kurt_vals = kurtosis(X_scaled, axis=0, fisher=True)
            feat_kurtosis = np.nanmean(kurt_vals) if not np.isnan(np.nanmean(kurt_vals)) else 0.0

            # 3. IQR Deviation (Standard deviation of IQRs across features)
            iqr_dev = np.std(iqr(X_scaled, axis=0))

            # 4. PC Eigenvariance (Mean explained variance of PCA)
            pca = PCA().fit(X_scaled)
            pc_eigen = np.mean(pca.explained_variance_)

            # 5. Target Entropy (Class Imbalance)
            target_entropy = entropy(y_clean.value_counts(normalize=True))

            # 6. Hopkins Statistic (Spatial Clustering)
            hopkins = calculate_hopkins(X_scaled)

            # 7 & 8. Silhouette and Davies-Bouldin (Requires >1 class)
            if y_clean.nunique() > 1:
                sil_score = silhouette_score(X_scaled, y_clean)
                db_score = davies_bouldin_score(X_scaled, y_clean)
            else:
                sil_score, db_score = 0, 0

            meta_features_list.append({
                "did": did,
                "name": name,
                "n_d_ratio": n_d_ratio,
                "feat_kurtosis": feat_kurtosis,
                "iqr_dev": iqr_dev,
                "pc_eigen": pc_eigen,
                "target_entropy": target_entropy,
                "hopkins": hopkins,
                "silhouette": sil_score,
                "davies_bouldin": db_score
            })

        except Exception as e:
            print(f"Error processing {name} (ID: {did}): {e}")

    # Convert results to DataFrame
    mf_df = pd.DataFrame(meta_features_list)

    mf_df.to_csv("Elite_8_Meta_Features_All_Processed_Datasets.csv", index=False)

    print(f"\nSuccessfully processed {len(mf_df)} out of {len(candidates)} datasets.")
    print("Saved full meta-feature table to: Elite_8_Meta_Features_All_Processed_Datasets.csv")

    if len(mf_df) < 25:
        raise ValueError(
            f"Only {len(mf_df)} datasets were successfully processed. "
            "At least 25 are required for the Top 25 K-Means selection."
        )

    # Task 3: Select 25 Diverse Datasets using K-Means Centroid Methodology
    print("\nApplying Representational Space Coverage (K-Means) to select Top 25...")
    features_only = mf_df.drop(columns=["did", "name"])

    # Final safety cleanup before clustering
    features_only = features_only.replace([np.inf, -np.inf], np.nan).fillna(0)

    features_standardized = StandardScaler().fit_transform(features_only)

    # METHODOLOGY: Group the total loaded datasets into 25 clusters.
    # Select the dataset closest to the center of each cluster to define our centroid pool.
    kmeans_25 = KMeans(n_clusters=25, random_state=42).fit(features_standardized)
    selected_indices_25 = []

    for i in range(25):
        cluster_points_idx = np.where(kmeans_25.labels_ == i)[0]
        cluster_center = kmeans_25.cluster_centers_[i].reshape(1, -1)
        distances = cdist(
            features_standardized[cluster_points_idx],
            cluster_center,
            metric="euclidean"
        )
        closest_idx = cluster_points_idx[np.argmin(distances)]
        selected_indices_25.append(closest_idx)

    top_25_df = mf_df.iloc[selected_indices_25].reset_index(drop=True)
    top_25_df.to_csv("Top_25_Representational_Centroid_Datasets.csv", index=False)

    # Task 4: Extract 20 Topological Centroids for Phase A (Discovery),
    # reserve all remaining successfully processed datasets for Phase B (Validation)
    print("Applying Stratified Spatial Splitting to isolate 20 Phase A Discovery centroids...")

    top_25_features = top_25_df.drop(columns=["did", "name"])
    top_25_features = top_25_features.replace([np.inf, -np.inf], np.nan).fillna(0)
    top_25_features = StandardScaler().fit_transform(top_25_features)

    # METHODOLOGY: Group the Top 25 centroid pool into 20 clusters.
    # The centroid-closest dataset in each cluster is extracted for Discovery (Phase A).
    # Every remaining successfully processed dataset not selected for Phase A is reserved
    # for Validation (Phase B).
    kmeans_20 = KMeans(n_clusters=20, random_state=42).fit(top_25_features)
    phase_A_indices = []

    for i in range(20):
        cluster_points_idx = np.where(kmeans_20.labels_ == i)[0]
        cluster_center = kmeans_20.cluster_centers_[i].reshape(1, -1)
        distances = cdist(
            top_25_features[cluster_points_idx],
            cluster_center,
            metric="euclidean"
        )
        closest_idx = cluster_points_idx[np.argmin(distances)]
        phase_A_indices.append(closest_idx)

    phase_A_df = top_25_df.iloc[phase_A_indices].reset_index(drop=True)

    # Reserve every successfully processed dataset not selected for Phase A
    # as Phase B Validation.
    phase_B_df = mf_df[
        ~mf_df["did"].isin(phase_A_df["did"])
    ].reset_index(drop=True)

    # Save the final splits

    GEN_DIR = r"C:\Users\John Arellano\PycharmProjects\Applied_Comp_Intel_Project\generated_files"

    # Ensure the directory exists
    os.makedirs(GEN_DIR, exist_ok=True)

    # Export to the new directory
    phase_a_df.to_csv(os.path.join(GEN_DIR, "Phase_A_Discovery_Datasets.csv"), index=False)
    phase_b_df.to_csv(os.path.join(GEN_DIR, "Phase_B_Validation_Datasets.csv"), index=False)
    print(f"Data successfully exported to {GEN_DIR}")

    print("\n--- SCRIPT COMPLETE ---")
    print(f"Total loaded from: {SAVE_DIR}")
    print("Top 25 centroid pool saved to: Top_25_Representational_Centroid_Datasets.csv")
    print("Phase A (20 Discovery Datasets) saved to: Phase_A_Discovery_Datasets.csv")
    print(f"Phase B ({len(phase_B_df)} Validation Datasets) saved to: Phase_B_Validation_Datasets.csv")

    print("\nPhase A (Discovery) Topological Centroids selected:")
    print(phase_A_df[["did", "name"]].to_string(index=False))

    print("\nPhase B (Validation) Datasets held out:")
    print(phase_B_df[["did", "name"]].to_string(index=False))


if __name__ == "__main__":
    main()
